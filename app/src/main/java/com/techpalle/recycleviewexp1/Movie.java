public class Movie {
    private String sno,actor,movie;



    public void setSno(String sno) {
        this.sno = sno;
    }

    public void setActor(String actor) {
        this.actor = actor;
    }

    public void setMovie(String movie) {
        this.movie = movie;
    }

    public String getSno() {
        return sno;
    }

    public String getActor() {
        return actor;
    }

    public String getMovie() {
        return movie;
    }
}
